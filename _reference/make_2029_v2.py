# ============================================================
# 干支データを (干支, 九星漢字, 数字) に分割するヘルパ
# 例: "火6" -> ("火", "6")
# ============================================================
def split_kyuusei(s: str):
    m = re.match(r'^(.+?)(\d+)$', s)
    if m:
        return m.group(1), m.group(2)
    return s, ""

